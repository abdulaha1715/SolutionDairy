<?php

namespace App\Observers;

use App\Events\ActivityEvent;
use App\Models\Tag;
use Illuminate\Support\Facades\Auth;

class TagObserver
{
    /**
     * Handle the Tag "created" event.
     *
     * @param  \App\Models\Tag  $tag
     * @return void
     */
    public function created(Tag $tag)
    {
        ActivityEvent::dispatch('New Tag Created', 'Tag', Auth::id());
    }

    /**
     * Handle the Tag "updated" event.
     *
     * @param  \App\Models\Tag  $tag
     * @return void
     */
    public function updated(Tag $tag)
    {
        ActivityEvent::dispatch('Tag '.$tag->id.' Updated', 'Tag', Auth::id());
    }

    /**
     * Handle the Tag "deleted" event.
     *
     * @param  \App\Models\Tag  $tag
     * @return void
     */
    public function deleted(Tag $tag)
    {
        ActivityEvent::dispatch('Tag '.$tag->id.' Deleted', 'Tag', Auth::id());
    }

    /**
     * Handle the Tag "restored" event.
     *
     * @param  \App\Models\Tag  $tag
     * @return void
     */
    public function restored(Tag $tag)
    {
        //
    }

    /**
     * Handle the Tag "force deleted" event.
     *
     * @param  \App\Models\Tag  $tag
     * @return void
     */
    public function forceDeleted(Tag $tag)
    {
        //
    }
}
